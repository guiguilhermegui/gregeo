<h3>Curso de Espeleo-vertical do EGB</h3>
<p>Do dia 03 ao dia 06 de agosto, oito pessoas do GREGEO participaram de parte do Curso de Técnicas Verticais em Cavernas oferecido pelo <a href="http://www.espeleogrupodebrasilia.org" title="Acesse o site do Espelo Grupo de Brasília">Espeleo Grupo de Brasília</a>,  ao Grupo.
Nos dias 3, 4, 5 ocorreram as aulas teóricas para conhecermos equipamentos: do que são feitos, como usá-los, cuidados que se deve ter; técnicas de subida, descida, ancoragem.. Enfim, aprendermos a subir e descer tudo com segurança. Mas, onde tudo pegou mesmo foi na aula prática: fomos para o <?php include("linkpedreira.php"); ?> dia 6 colocar em prática tudo que vimos.
Foi um dia de paredão de aproximadamente 30 metros de altura. Subindo, descendo, aprendendo a passar por desvios, fracionamentos… </p>
