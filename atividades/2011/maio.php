<h3>Projeto Morro da Pedreira o que já encontramos!</h3>
<h4>Fizemos uma saída para prospecção na área e foram encontrada 6 cavidades, a maior delas foi a que chamamos de Caveira, a entrada sugeriu o nome rsrs</h4>
<p>Fomos um grande grupo entre pessoas que estavam ali a primeira vez e membros que já andaram pelo Morro muitas vezes.
Logo após, exatamente no dia 07/05/2011, nove membros do gregeo partiram para o <?php include("linkpedreira.php"); ?>, localizado na região da fercal, Sobradinho-DF, com o objetivo de topografar as novas cavernas encontradas na região.
A equipe saiu do campus da UnB às 8hs e chegou no local de trabalho às 9hs.
Inicialmente, tivemos algumas dificuldades para achar as cavernas que seriam topografadas, para tal a equipe se dividiu em dois grupos, um seguiu pela parte baixa do morro, enquanto o outro rumo no topo.
A caverna da caveira foi localizada às 11h por um dos grupos, simultaneamente, em cima do morro, outro grupo achou outras duas cavernas.
Após o almoço em frente a caverna da caveira, a equipe decidiu por começar a topografia da caverna da caveira, parcialmente concluida após o termino do dia de trabalho.
Este é o primeiro campo de do projeto de atualização dos dados espeleológicos do morro da pedreira. A próxima saída de campo ocorrerá no  dia 15/05, domingo.
Estiveram presentes os 7 membros do grupo e 2 novatos(molhos, como dizemos não-membros que estão ingressando agora no GREGEO).</p>
