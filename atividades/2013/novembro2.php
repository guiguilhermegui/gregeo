<h4>Saída de campo da matéria de Geologia Geral com estudantes de Ciências Naturais</h4>
<h5>30/11/2013</h5>
<p>Acompanhamos o professor <a target="_blank" href="http://buscatextual.cnpq.br/buscatextual/visualizacv.do?metodo=apresentar&id=K4739137P9" title="Currículo Lattes">Rodrigo Santucci</a> na saída de campo com seus alunos da matéria de Geologia Geral.
A turma é formada por estudantes do curso de Ciências Naturais da <span class="explicacao" title="Campus de Planaltina, Universidade de Brasília">FUP-UnB</span></p>
<p>A saída é realizada semestralmente, e tem como objetivo estudar a formação de cavernas e todo o processo dinâmico presente no sistema <a href="index.php?pagina=espeleologia" title="Saiba mais sobre sistema cárstico e formação de cavernas">cárstico</a>.</p>
