<h4>Ida à Gruta Volks Club, no Lago Sul</h4>
<h5>22.03.2013</h5>
<p>A Gruta Volks Club até então desconhecida pelos membros do Gregeo situa-se em uma área de fácil acesso no Lago Sul. É uma caverna formada em <span class="explicacao" title="Ritmito é uma rocha sedimentar">ritmito</span>. Construções muito próximas apresentam-se como risco à conservação da caverna.</p>
<p>Como a Gruta é bem pequena em pouco tempo pudemos explorá-la por completo. Em seguida seguimos à cachoeira do Tororó.</p>
