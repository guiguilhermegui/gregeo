<h4>Gruta dos Ecos</h4>
<h5>23/11/2013</h5>
<p>A Gruta dos Ecos é uma caverna de beleza rara, é formada em micaxisto, sendo a maior do  mundo nesse tipo de rocha. A Gruta também abriga o maior lago subterrâneo
da América Latina. Desde 2001, através da portaria Ibama nº 014 de 23/02/2001 está fechada a visitação turística à caverna, tendo em vista o degradamento ambiental que estava sendo causado pela atividade descontrolada.</p>
<p>Nossa expedição foi realizada com o objetivo de realizar o reconhecimento das características técnicas da caverna e de sua atual condição ambiental.</p>

