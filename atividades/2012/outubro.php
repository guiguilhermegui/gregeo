<h4>Acampamento no Morro da Pedreira</h4>
<h5>20/10 - 21/10/2012</h5>
<div class="plongo">
<p>Mais uma vez fomos ao <?php include("linkpedreira.php"); ?> em busca de cavernas novas, porém deste vez na face sudeste do morro, conhecido como Morro do Urubu. A equipe, composta pelo Rafael Grudka (Côxa), Túlio Gabriel (Tutão), André Cunha (Kaverna) e Hugo Pardal.</p>
<p>No primeiro dia encontramos as cavernas "Cortina Sagrada" e "Anos Douradas", grutas essas já registradas e topografadas pelo Gregeo. As visitamos rapidamente e prosseguimos com nossa <span class="explicacao" title="Busca por cavernas desconhecidas">prospecção</span> no Morro.
No topo do morro encontramos duas pequenas cavernas com pequeno desenvolvimento linear e dois abismos bastante promissores.</p>
<p>No segundo dia iniciamos nos encontrando com dois membros do <span class="explicacao" title="Espelo Grupo de Brasília">EGB</span>, para quem mostramos a localização de algumas grutas na região. Durante a tarde realizamos um treino de vertical nas árvores e em seguida nos dirigimos às cavernas "Castelo" e "Cortina Sagrada".</p>
