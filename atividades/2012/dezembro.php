<h4>Gruta das Andorinhas</h4>
<h5>01.12.2012</h5>
<div class="plongo">
<p>No dia 1º de dezembro realizamos uma visita à gruta Andorinhas, no município de Formoso-GO. A entrada dessa caverna é uma <span class="explicacao" title="Dolinas são depressões em formato circular formados pela dissolução da rocha. (mais informações na página &quot;A Espeleolgia&quot;)">dolina</span> de colapso em formato circular, com aproximadamente 60 metros de diâmetro e 250 metros de profundidade. Essa entrada foi formada pelo desabamento do teto da caverna devido ao peso do solo sobre a rocha dissolvida. A gruta das Andorinhas está localizada próxima ao Buraco das Araras.</p>
