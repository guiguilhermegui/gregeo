<h4>Painel no II Encontro Brasileiro de Estudos do Carste</h4>
<div class="plongo">
<p>GREGEO-UnB apresentou painel no II Encontro Brasileiro de Estudos do Carste – Carste 2007.</p>
<p>O II Encontro Brasileiro de Estudos do Carste – Carste 2007, que aconteceu nessa semana em São Paulo, reuniu mais uma vez cientistas e profissionais das áreas espeleológicas, com o objetivo de promover uma avaliação do “estado da arte” do conhecimento científico relacionados aos estudos do carste.</p>
<p>Trata-se de um dos mais importantes eventos sobre carste e outros ambientes subterrâneos no país, contou com a participação de pesquisadores e grupos de espeleologia de todo o Brasil.</p>
<p>O GREGEO-UnB foi muito bem representado no evento, pelo seu presidente João Antônio Mallmann e por Letícia Moraes, que apresentou no evento o painel “Integração de informações geológicas e espeleológicas do Vale do Rio Areia, Unaí-MG, em ambiente de Sistema de Informações Geográficas-SIG”.</p>
<p>Na abertura do evento foi ressaltada a importância dos estudos relacionados ao carste, e principalmente da interdiciplinariedade dos estudos, a fim de complementar o conhecimento sobre o carste, o desenvolvimento sustentável da exploração mineral e a melhoria da legislação ambiental.</p>


