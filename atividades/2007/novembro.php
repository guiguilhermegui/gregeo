<h4>Ida ao Vale do Rio Areia junto ao DEA</h4>
<div class="plongo">
<p>GREGEO-UnB em parceria com DEA, levou no último domingo, alunos da UnB para conhecerem o Vale do Rio Areia em Unaí-MG</p>
<p>Nó último domingo, 25 de novembro de 2007, o GREGEO-UnB, numa pareceria que já dura há mais de três anos com o DEA (Departamento de Esportes, Arte e Cultura da UnB), levou 30 alunos da Universidade de Brasília, para conhecerem uma das grutas do vale do Rio Areia, em Unaí-MG.</p>
<p>Segundo, João Mallmann, então presidente do GREGEO-UnB, o resultado da viagem foi extremamente positivo. “Esse tipo de passeio, sempre é positivo, porque podemos mostrar aos alunos, na prática, a importância da preservação deste tipo de ambiente e como fazê-lo”, falou.</p>
