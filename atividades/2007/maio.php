<h4>Exposição de fotos na semana da Calourada da UnB</h4>
<p>Do dia 22 ao 25 de maio de 2007, o GREGEO, aproveitando a Semana da Calourada que acontece na UnB, apresentou uma exposição de fotos na entrada do ICC norte, conhecida como Ceubinho. A exposição com fotos das atividades de campo do grupo desde o seu início, em 1985, ocorreu todos os dias das 12h às 14h.  Foi de muita importância para mostrar aos novos integrantes da comunidade UnB a importância da espeleologia e das atividades do GREGEO.</p>
